void StoryManager::play() {
	for (int i = 0; i < scenes.size(); ++i) {
		system("cls");
		cout << WIDTH_BORDER << endl;
		cout << "                                STORY: " << i + 1 << " / 7" << endl;
		cout << WIDTH_BORDER << endl << endl;

		cout << scenes[i].asciiArt << endl;
		cout << "  " << scenes[i].description << endl;
		cout << "--------------------------------------------------------------------------------" << endl;

		// 1. 2초 동안 대기
		Sleep(200);

		// 2. 중요: 대기 시간 동안 쌓인 키보드 버퍼를 모두 비웁니다.
		// _kbhit()은 키보드 입력이 버퍼에 있는지 확인하고, 있으면 _getch()로 다 읽어버려서 없앱니다.
		while (_kbhit()) { _getch(); }

		// 3. 안내 문구 출력
		cout << "\n                         [ Enter를 눌러 진행 ]" << endl;

		// 4. 이제부터 들어오는 엔터 입력만 대기
		while (_getch() != ENTER);
	}

	system("cls");
	cout << "\n\n\n\n\n";
	cout << "                    당신은 이제 김OO으로 살며" << endl;
	cout << "               매일 불어나는 이자를 갚아야 합니다." << endl;
	Sleep(3000);
	while (_kbhit()) { _getch(); } // 게임 진입 전에도 버퍼를 비워 오작동 방지
}